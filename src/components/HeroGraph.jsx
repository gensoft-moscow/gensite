function GraphLayer({ className, delay, nodes, edges }) {
  return (
    <g className={`graph-layer ${className}`}>
      <g className="graph-edges">
        {edges.map(([x1, y1, x2, y2], index) => (
          <line
            key={`${x1}-${y1}-${x2}-${y2}`}
            x1={x1}
            y1={y1}
            x2={x2}
            y2={y2}
            pathLength="1"
            style={{ '--edge-delay': `${delay + index * 0.045}s` }}
          />
        ))}
      </g>
      <g className="graph-nodes">
        {nodes.map(([x, y], index) => (
          <circle
            className="graph-node"
            key={`${x}-${y}`}
            cx={x}
            cy={y}
            r={index % 4 === 0 ? 3.5 : 2.3}
            style={{ '--node-delay': `${delay + 0.08 + index * 0.075}s` }}
          />
        ))}
      </g>
    </g>
  )
}

function HeroGraph() {
  return (
    <div className="graph-field" aria-hidden="true">
      <svg className="hero-graph" viewBox="0 0 680 660" role="presentation">
        {graphLayers.map((layer) => (
          <GraphLayer key={layer.className} {...layer} />
        ))}
      </svg>
    </div>
  )
}

export default HeroGraph
